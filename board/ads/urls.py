# =========================
# URL МАРШРУТЫ ПРИЛОЖЕНИЯ ADS
# Файл: board/ads/urls.py
#
# Здесь описаны ВСЕ маршруты,
# связанные с объявлениями.
# =========================

# path() — связывает URL и функцию-вьюху
from django.urls import path

# Импортируем вьюхи из текущего приложения
from . import views


# =========================
# СПИСОК URL ЭТОГО ПРИЛОЖЕНИЯ
# =========================
urlpatterns = [

    # =====================================================
    # /ads/
    # ЛЕНТА ОБЪЯВЛЕНИЙ
    # =====================================================
    path(
        "",
        views.ads_list,
        name="ads_list"
    ),

    # =====================================================
    # /ads/create/
    # СОЗДАНИЕ ОБЪЯВЛЕНИЯ
    # =====================================================
    path(
        "create/",
        views.create_ad,
        name="create_ad"
    ),

    # =====================================================
    # /ads/<id>/edit/
    # РЕДАКТИРОВАНИЕ ОБЪЯВЛЕНИЯ
    # =====================================================
    path(
        "<int:ad_id>/edit/",
        views.edit_ad,
        name="edit_ad"
    ),

    # =====================================================
    # /ads/<id>/archive/
    # СНЯТЬ ОБЪЯВЛЕНИЕ В АРХИВ
    # =====================================================
    path(
        "<int:ad_id>/archive/",
        views.archive_ad,
        name="archive_ad"
    ),

    # =====================================================
    # /ads/<id>/restore/
    # ВЕРНУТЬ ОБЪЯВЛЕНИЕ ИЗ АРХИВА
    # =====================================================
    path(
        "<int:ad_id>/restore/",
        views.restore_ad,
        name="restore_ad"
    ),

    # =====================================================
    # /ads/<id>/delete/
    # УДАЛЕНИЕ ОБЪЯВЛЕНИЯ
    # =====================================================
    path(
        "<int:ad_id>/delete/",
        views.delete_ad,
        name="delete_ad"
    ),

    # =====================================================
    # /ads/<id>/
    # СТРАНИЦА ОДНОГО ОБЪЯВЛЕНИЯ
    # =====================================================
    path(
        "<int:ad_id>/",
        views.ad_detail,
        name="ad_detail"
    ),
]